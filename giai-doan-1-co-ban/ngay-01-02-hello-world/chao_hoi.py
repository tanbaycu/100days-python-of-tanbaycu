print(f"Rất vui được gặp bạn, {ten}!")

# Thêm một số lời chào thân thiện
print(f"Chúc {ten} một ngày tốt lành!")
print(f"Hy vọng {ten} sẽ thích học Python!")

print("\n" + "=" * 30)
print(f"  Chào mừng {ten} đến với Python!")
